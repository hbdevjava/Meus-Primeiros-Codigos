package fundamentos.Operadores.logicos;

public class Logicos {
	public static void main(String[] args) {
		boolean cond1 = true;
		boolean cond2 = 3 > 7;
		System.out.println(cond1 && cond2);// E
		System.out.println(cond1 || cond2);// OU
		System.out.println(cond1 ^ cond2);// XOR exclusivo
		System.out.println( !!cond1 );//dupla negaçao
		System.out.println( !cond2 );//NOT negaçao (operador unario)
		
		
		
		
		
		
		
		
		
		
	}
}
