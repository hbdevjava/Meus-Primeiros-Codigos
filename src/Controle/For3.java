package Controle;

public class For3 {
	public static void main(String[] args) {
		
		//LAÇO USADO PRA IR DE 0 A ALGUM NUMERO SEM USAR > <;
		for (int i = 0; i < 10; i++) {
		   for(int j = 0; j < 10; j++) {
			   System.out.printf("\n[%d %d]", i, j);
		   }
		   System.out.println();
	  }
				
		
		
   }
}