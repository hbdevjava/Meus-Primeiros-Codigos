package Controle;

public class WhileDeterminado {
      public static void main(String[] args) {
	
    	  int contador = 0;
    	  while (contador <= 20) { // WHILE É UMA ESTRUTURA MAIS VOLTADA PRA QUANTIDADES INDERTERMINADA DE VEZES;
    		  System.out.printf("i = %d\n", contador);
    		  contador+= 2;
    		  
    	  }
    	  
     }
}
